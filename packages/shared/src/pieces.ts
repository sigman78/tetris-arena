export function getKickTests(piece: PieceType, from: number, to: number): RotationPoint[] {
  if (piece === "O") {
    return [{ x: 0, y: 0 }];
  }

  const key = `${from}>${to}`;
  return piece === "I" ? I_KICKS[key] ?? [{ x: 0, y: 0 }] : JLSTZ_KICKS[key] ?? [{ x: 0, y: 0 }];
}
